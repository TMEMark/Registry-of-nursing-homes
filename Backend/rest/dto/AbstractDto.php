<?php

namespace dto;

use JsonSerializable;

abstract class AbstractDto implements JsonSerializable
{
    public int $id;

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @param int $id
     */
    public function setId(int $id): void
    {
        $this->id = $id;
    }
}