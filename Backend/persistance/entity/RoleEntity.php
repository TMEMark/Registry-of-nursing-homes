<?php
include('AbstractEntity.php');
class RoleEntity extends AbstractEntity{
    private String $name;

	/**
	 * @return string
	 */
	public function getName(): string {
		return $this->name;
	}
	
	/**
	 * @param string $name 
	 * @return self
	 */
	public function setName(string $name): self {
		$this->name = $name;
		return $this;
	}
}
?>